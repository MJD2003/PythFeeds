// Helper to generate realistic sparkline data
function generateSparkline(base: number, trend: number): number[] {
  const prices: number[] = [];
  let price = base;
  for (let i = 0; i < 168; i++) {
    price += (Math.random() - 0.48) * base * 0.01 + trend * base * 0.0001;
    prices.push(Math.max(price, base * 0.8));
  }
  return prices;
}

